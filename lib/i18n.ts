export type Lang = 'en' | 'es';
export type StringKey = keyof typeof strings;

export function t(key: StringKey, lang: Lang = 'en'): string {
  const entry = strings[key];
  if (!entry) return key;
  return entry[lang] ?? entry.en;
}

const MONTH_ABBR_KEYS: StringKey[] = ['month.jan','month.feb','month.mar','month.apr','month.mayShort','month.jun','month.jul','month.aug','month.sep','month.oct','month.nov','month.dec'];

export function getMonthAbbr(lang: Lang): string[] {
  return MONTH_ABBR_KEYS.map(k => t(k, lang));
}
